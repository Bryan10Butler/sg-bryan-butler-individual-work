package practiceLessons;

public class MethodToMadness {
    public static void main(String[] args) {
        eatMe();
        drinkMe();
        System.out.println("\n - Lewis Carrol, Alice in Wonderland");
    }

    static void eatMe() {
        System.out.println(" 'But I don't want to go amount mad people, ' Alice remarked");
        System.out.println(" 'Oh, you can't help that, ' said the cat.");
        System.out.println(" 'We're all mad here. I'm mad. You're mad.' ");
    }

    static void drinkMe() {
        System.out.println(" 'How do you know i'm mad?' You're mad.");
        System.out.println(" 'You mus be, said the cat, 'or you wouldn't have come here.' ");
    }
}
